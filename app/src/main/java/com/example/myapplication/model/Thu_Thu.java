package com.example.myapplication.model;

public class Thu_Thu {
    private String mathuthu ;
    private String hotenthuthu;
    private String matkhau;

    public String getMathuthu() {
        return mathuthu;
    }

    public void setMathuthu(String mathuthu) {
        this.mathuthu = mathuthu;
    }

    public String getHotenthuthu() {
        return hotenthuthu;
    }

    public void setHotenthuthu(String hotenthuthu) {
        this.hotenthuthu = hotenthuthu;
    }

    public String getMatkhau() {
        return matkhau;
    }

    public void setMatkhau(String matkhau) {
        this.matkhau = matkhau;
    }

    public Thu_Thu() {
    }

    public Thu_Thu(String mathuthu, String hotenthuthu, String matkhau) {
        this.mathuthu = mathuthu;
        this.hotenthuthu = hotenthuthu;
        this.matkhau = matkhau;
    }
}
